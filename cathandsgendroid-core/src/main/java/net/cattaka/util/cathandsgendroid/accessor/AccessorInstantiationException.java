package net.cattaka.util.cathandsgendroid.accessor;

public class AccessorInstantiationException extends RuntimeException {
	private static final long serialVersionUID = 1L;

	public AccessorInstantiationException() {
		super();
	}

	public AccessorInstantiationException(String message, Throwable cause) {
		super(message, cause);
	}

	public AccessorInstantiationException(String message) {
		super(message);
	}

	public AccessorInstantiationException(Throwable cause) {
		super(cause);
	}

}
